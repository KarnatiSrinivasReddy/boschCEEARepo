package de.hybris.ceea.fulfilmentprocess.request;

/**
 * @author hybris2go
 *
 */
public class CEEAHotspotRequest
{
	private String hotspotid;
	private String transform;
	private String scale;
	/**
	 * @return the hotspotid
	 */
	public String getHotspotid()
	{
		return hotspotid;
	}
	/**
	 * @param hotspotid the hotspotid to set
	 */
	public void setHotspotid(String hotspotid)
	{
		this.hotspotid = hotspotid;
	}
	/**
	 * @return the transform
	 */
	public String getTransform()
	{
		return transform;
	}
	/**
	 * @param transform the transform to set
	 */
	public void setTransform(String transform)
	{
		this.transform = transform;
	}
	/**
	 * @return the scale
	 */
	public String getScale()
	{
		return scale;
	}
	/**
	 * @param scale the scale to set
	 */
	public void setScale(String scale)
	{
		this.scale = scale;
	}
	/**
	 * @return the rotation
	 */
	public String getRotation()
	{
		return rotation;
	}
	/**
	 * @param rotation the rotation to set
	 */
	public void setRotation(String rotation)
	{
		this.rotation = rotation;
	}
	/**
	 * @return the hotspotText
	 */
	public String getHotspotText()
	{
		return hotspotText;
	}
	/**
	 * @param hotspotText the hotspotText to set
	 */
	public void setHotspotText(String hotspotText)
	{
		this.hotspotText = hotspotText;
	}
	/**
	 * @return the hotspotimage
	 */
	public String getHotspotimage()
	{
		return hotspotimage;
	}
	/**
	 * @param hotspotimage the hotspotimage to set
	 */
	public void setHotspotimage(String hotspotimage)
	{
		this.hotspotimage = hotspotimage;
	}
	/**
	 * @return the hotspotMedia
	 */
	public String getHotspotMedia()
	{
		return hotspotMedia;
	}
	/**
	 * @param hotspotMedia the hotspotMedia to set
	 */
	public void setHotspotMedia(String hotspotMedia)
	{
		this.hotspotMedia = hotspotMedia;
	}
	private String rotation;
	private String hotspotText;
	private String hotspotimage;
	private String hotspotMedia;
}
